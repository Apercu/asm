extern _ft_strlen

global _ft_puts

section .text

_ft_puts:

	push rdi
	push rsi

  mov rcx, rdi

  call _ft_strlen

  mov rdx, rax
  mov rax, 0x2000004
  mov rdi, 1
  mov rsi, rcx
  syscall

  jc error

  mov rax, 0x2000004
  mov rdi, 1
	lea rsi, [rel newline]
  mov rdx, 1
  syscall

  jc error

	pop rsi
	pop rdi

	mov rax, 1

  ret

error:

	pop rsi
	pop rdi

  mov rax, -1
  ret

section .data

newline: db 0xa
