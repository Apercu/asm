extern _ft_isalpha

global _ft_tolower

section .text

_ft_tolower:

  mov rcx, rdi

  call _ft_isalpha

  cmp rax, 1
  je tolower

  mov rax, rdi

out:
  ret

tolower:
  mov rax, rdi
  or rax, 0x20
  jmp out
