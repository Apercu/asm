extern _ft_strlen

global _ft_strncat

section .text

_ft_strncat:

  mov rcx, rdi

  call _ft_strlen

  mov rcx, rax
  xor rax, rax

while:
  cmp byte [rsi + rax], 0
  je out

	cmp rdx, rax
	je out

  mov r9b, byte [rsi + rax]

  add rax, rcx

  mov byte [rdi + rax], r9b

  sub rax, rcx

  inc rax
  jmp while

out:
  add rax, rcx
  mov byte [rdi + rax], 0
  mov rax, rdi
  ret
